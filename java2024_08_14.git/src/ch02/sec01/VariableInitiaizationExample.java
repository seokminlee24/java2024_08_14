package ch02.sec01;

public class VariableInitiaizationExample {
    public static void main(String[] args) {
        // 변수 value 선언
        int value = 30; // 변수에 값을 저장 안하면 에러

        //연산 결과를 변수 result 의 초기값으로 대입
        int result = value + 10; //result = 30 + 10

        //변수 result 값을 읽고 콘솔에 출력
        System.out.println(result); // 40
    }
}
